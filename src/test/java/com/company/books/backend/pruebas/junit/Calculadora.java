package com.company.books.backend.pruebas.junit;

public class Calculadora {

    float suma(float a, float b){
        return a + b;
    }

    float resta(float a, float b){
        return a - b;
    }

    float multiplicacion(float a, float b){
        return a * b;
    }

    float dividir(float a, float b){
        return a / b;
    }
}
